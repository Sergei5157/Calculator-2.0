public class Operation extends Calculator{
    public  int sum(int one, int two){
        return one + two;
    }
    public  int difference(int one, int two){
        return one - two;
    }
    public  int productOfNumbers(int one, int two){
        return one * two;
    }
    public  int division(int one, int two){
        return one / two;
    }

}